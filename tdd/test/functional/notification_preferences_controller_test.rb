require 'test_helper'

class NotificationPreferencesControllerTest < ActionController::TestCase
=begin
  setup do
    @notification_preference = notification_preferences(:one)
  end

  test "should get index" do
    get :index
    assert_response :success
    assert_not_nil assigns(:notification_preferences)
  end

  test "should get new" do
    get :new
    assert_response :success
  end

  test "should create notification_preference" do
    assert_difference('NotificationPreference.count') do
      post :create, :notification_preference => @notification_preference.attributes
    end

    assert_redirected_to notification_preference_path(assigns(:notification_preference))
  end

  test "should show notification_preference" do
    get :show, :id => @notification_preference.to_param
    assert_response :success
  end

  test "should get edit" do
    get :edit, :id => @notification_preference.to_param
    assert_response :success
  end

  test "should update notification_preference" do
    put :update, :id => @notification_preference.to_param, :notification_preference => @notification_preference.attributes
    assert_redirected_to notification_preference_path(assigns(:notification_preference))
  end

  test "should destroy notification_preference" do
    assert_difference('NotificationPreference.count', -1) do
      delete :destroy, :id => @notification_preference.to_param
    end

    assert_redirected_to notification_preferences_path
  end
=end
end
